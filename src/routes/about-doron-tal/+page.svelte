				<li>
					<h4>Ānāpānasati Meditation Method (2020 - Present)</h4>
					<p>
						Practice of the sixteen-step meditation instructions of the Buddha. These steps are
						somewhat open to interpretation - multiple perspectives have helped form an approach and
						an interpretation of these instructions.
					</p>
				</li>
				<li>
					<h4>Ajahn Brahm's Meditation Method (2019 - 2020)</h4>
					<p>
						Following a 2019 retreat in Thailand based on Ajahn Brahm's approach from his book
						"Mindfulness, Bliss and Beyond", practiced Brahm's method for about two years. Since
						then, I follow the Buddha's 16-step method as described in the Ānāpānasati Sutta,
						(Majjhima Nikaya 118), more or less along the instruction of Ajahn Buddhadasa
					</p>
				</li>
				<li>
					<h4>Walking Meditation (2019 - Present)</h4>
					<p>
						Began with Yuttadhammo Bhikkhu's Theravada <a
							href="https://www.youtube.com/watch?v=_IFvablc6EI">walking meditation instructions</a
						> (one note per step), added Bhikkhu Bodhi's three-note method (picking, pushing, placing)
						and a six-step technique (helps crowd out thoughts). The approach varies depending on the
						mental state at the start of each session.
					</p>
				</li>
				<li>
					<h4>Metta Practice (2015 - Present)</h4>
					<p>
						Practiced Metta at Tibet House in New York City under the guidance of <a
							href="https://www.rachelhammerman.com/">Rachel Hammerman</a
						>. Since 2019, I’ve been attending
						<a href="https://www.baus.org/en/group-meditation-over-zoom/"
							>Bhikkhu Bodhi's weekly Metta
						</a> sessions.
					</p>
				</li>
				<li>
					<h4>A Talk by His Holiness the Dalai Lama (1991)</h4>
					<p>
						In 1991, His Holiness the Dalai Lama gave a transformative talk titled <a
							href="https://www.youtube.com/watch?v=_2G-4m4y8Ss">"Overcoming Differences"</a
						>. This experience inspired me to try and follow his advice regarding the relinquishment
						of anger as a source of happiness, and to continue following some of his work.
					</p>
				</li>
				<li>
					<h4>Qigong (1989 - Present)</h4>
					<p>
						Learned a Qigong (Chi Kung) set for spine healing, strengthening and stretching, created
						by Master Jiao Guo Ryu, from <a
							href="https://www.eomega.org/workshops/teachers/robert-tangora"
						>
							Robert Tangora</a
						>.
					</p>
				</li>
				<li>
					<h4>Tai Chi (1988 - Present)</h4>
					<p>
						Tai Chi is moving meditation. I mainly practice the
						<a href="https://www.youtube.com/watch?v=3_BKwlpOAkk">
							Yang Style Short Form of Prof. Chen Man Ching</a
						>
						and have studied under the instruction of Katherine Hannah,
						<a href="https://www.youtube.com/watch?v=6rLG3Ys-wIU">Maggie Newman</a>,
						<a
							href="https://www.facebook.com/UCBTaiji/videos/william-dere-pushing-hands-principles/2454512608101776/"
							>Sifu William Dere</a
						>, and since the late 80's, with
						<a href="https://www.williamccchen.com/">Great Grandmaster William C. C. Chen</a>.
					</p>
				</li>
				<li>
					<h4>Breathwork & Body Scans (Mid 1980's - Present)</h4>
					<p>
						Yoga breathing exercises to calm the body via abdominal breathing, counting, and
						visualization. Also started practicing the body scan.
					</p>
